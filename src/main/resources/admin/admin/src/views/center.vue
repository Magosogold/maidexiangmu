// 数字，邮件，手机，url，身份证校验
import { isNumber,isIntNumer,isEmail,isMobile,isPhone,isURL,checkIdCard } from "@/utils/validate";

export default {
  data() {
    return {
      ruleForm: {},
      flag: '',
      usersFlag: false,
      toubiaodanweitoubiaojibieOptions: [],
    };
  },
  mounted() {
    var table = this.$storage.get("sessionTable");
    this.flag = table;
    this.$http({
      url: `${this.$storage.get("sessionTable")}/session`,
      method: "get"
    }).then(({ data }) => {
      if (data && data.code === 0) {
        this.ruleForm = data.data;
      } else {
        this.$message.error(data.msg);
      }
    });
    this.toubiaodanweitoubiaojibieOptions = "一级,二级,三级".split(',')
  },
  methods: {
    zhaobiaodanweizhaobiaozigeUploadChange(fileUrls) {
        this.ruleForm.zhaobiaozige = fileUrls;
    },
    pingbiaozhuanjiatouxiangUploadChange(fileUrls) {
        this.ruleForm.touxiang = fileUrls;
    },
    onUpdateHandler() {
      if((!this.ruleForm.zhaobiaodanwei)&& 'zhaobiaodanwei'==this.flag){
        this.$message.error('招标单位不能为空');
        return
      }
      if((!this.ruleForm.mima)&& 'zhaobiaodanwei'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
        if(this.ruleForm.zhaobiaozige!=null) {
                this.ruleForm.zhaobiaozige = this.ruleForm.zhaobiaozige.replace(new RegExp(this.$base.url,"g"),"");
        }
      if( 'zhaobiaodanwei' ==this.flag && this.ruleForm.zixundianhua&&(!isPhone(this.ruleForm.zixundianhua))){
        this.$message.error(`咨询电话应输入电话格式`);
        return
      }
      if((!this.ruleForm.zhuanjiaxingming)&& 'pingbiaozhuanjia'==this.flag){
        this.$message.error('专家姓名不能为空');
        return
      }
      if((!this.ruleForm.mima)&& 'pingbiaozhuanjia'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
        if(this.ruleForm.touxiang!=null) {
                this.ruleForm.touxiang = this.ruleForm.touxiang.replace(new RegExp(this.$base.url,"g"),"");
        }
      if( 'pingbiaozhuanjia' ==this.flag && this.ruleForm.nianling&&(!isIntNumer(this.ruleForm.nianling))){
       this.$message.error(`年龄应输入整数`);
        return
      }
      if( 'pingbiaozhuanjia' ==this.flag && this.ruleForm.lianxifangshi&&(!isMobile(this.ruleForm.lianxifangshi))){
        this.$message.error(`联系方式应输入手机格式`);
        return
      }
      if((!this.ruleForm.toubiaodanwei)&& 'toubiaodanwei'==this.flag){
        this.$message.error('投标单位不能为空');
        return
      }
      if((!this.ruleForm.mima)&& 'toubiaodanwei'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
      if( 'toubiaodanwei' ==this.flag && this.ruleForm.lianxidianhua&&(!isMobile(this.ruleForm.lianxidianhua))){
        this.$message.error(`联系电话应输入手机格式`);
        return
      }
      if('users'==this.flag && this.ruleForm.username.trim().length<1) {
	this.$message.error(`用户名不能为空`);
        return	
      }
      this.$http({
        url: `${this.$storage.get("sessionTable")}/update`,
        method: "post",
        data: this.ruleForm
      }).then(({ data }) => {
        if (data && data.code === 0) {
          this.$message({
            message: "修改信息成功",
            type: "success",
            duration: 1500,
            onClose: () => {
            }
          });
        } else {
          this.$message.error(data.msg);
        }
      });
    }
  }
};
</script>
