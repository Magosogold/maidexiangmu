        <el-form-item :style='{"width":"50%","margin":"0 0 20px 0"}'   v-if="flag=='zhaobiaodanwei'"  label="联系人" prop="lianxiren">
          <el-input v-model="ruleForm.lianxiren"               placeholder="联系人" clearable></el-input>
        </el-form-item>
        <el-form-item :style='{"width":"50%","margin":"0 0 20px 0"}' v-if="flag=='zhaobiaodanwei'" label="招标资格" prop="zhaobiaozige">
          <file-upload
          tip="点击上传招标资格"
          action="file/upload"
