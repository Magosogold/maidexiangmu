          <el-input v-model="ruleForm.zixundianhua"               placeholder="咨询电话" clearable></el-input>
        </el-form-item>
        <el-form-item :style='{"width":"50%","margin":"0 0 20px 0"}'   v-if="flag=='zhaobiaodanwei'"  label="办公地址" prop="bangongdizhi">
          <el-input v-model="ruleForm.bangongdizhi"               placeholder="办公地址" clearable></el-input>
        </el-form-item>
        <el-form-item :style='{"width":"50%","margin":"0 0 20px 0"}'   v-if="flag=='pingbiaozhuanjia'"  label="专家姓名" prop="zhuanjiaxingming">
          <el-input v-model="ruleForm.zhuanjiaxingming" readonly              placeholder="专家姓名" clearable></el-input>
        </el-form-item>
        <el-form-item :style='{"width":"50%","margin":"0 0 20px 0"}' v-if="flag=='pingbiaozhuanjia'" label="头像" prop="touxiang">
          <file-upload
          tip="点击上传头像"
          action="file/upload"
          :limit="3"
          :multiple="true"
          :fileUrls="ruleForm.touxiang?ruleForm.touxiang:''"
          @change="pingbiaozhuanjiatouxiangUploadChange"
          ></file-upload>
        </el-form-item>
        <el-form-item :style='{"width":"50%","margin":"0 0 20px 0"}'   v-if="flag=='pingbiaozhuanjia'"  label="年龄" prop="nianling">
